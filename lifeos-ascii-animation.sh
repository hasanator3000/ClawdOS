# Color codes
colors=(
  "\033[1;36m"  # Cyan
  "\033[1;35m"  # Magenta
  "\033[1;32m"  # Green
  "\033[1;33m"  # Yellow
  "\033[1;34m"  # Blue
  "\033[1;31m"  # Red
)

reset="\033[0m"

echo "Starting LifeOS ASCII Art Animation..."
echo "Press Ctrl+C to stop"
sleep 2

# Animation loop
frame_index=0
color_index=0

while true; do
  clear

  # Get current frame and color
  current_frame="${frames[$frame_index]}"
  current_color="${colors[$color_index]}"

  # Print colored frame
  echo -e "${current_color}${current_frame}${reset}"

  # Update indices
  frame_index=$(( (frame_index + 1) % ${#frames[@]} ))
  color_index=$(( (color_index + 1) % ${#colors[@]} ))

  sleep 0.5
done
